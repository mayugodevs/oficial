                    var collectionbcl = document.getElementsByClassName("lictt");
                    for (i = 0; i < collectionbcl.length; i++) {
                        if (varbcl[i].classList.contains('hdrtalks')) {
                            varbcl[i].classList.remove('hdrtalks');
                            varbcl[i].classList.add('border-bordertalks');
                            collectionbcl[i].classList.add("hidden");
                        }
                    }
                    document.getElementById(lcursosa).classList.add('hdrtalks');
                    document.getElementById(lcursosa).classList.remove('border-bordertalks');
                    document.getElementById(llcursosa).classList.remove("hidden");
            }
